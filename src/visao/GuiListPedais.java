    Ciclistas ciclista = new Ciclistas();
    protected ArrayList<Pedal> varLista = ciclista.listarPedais();
    String numContaOrigem = "";
    
    
    DefaultTableModel dtm = new DefaultTableModel(
              new Object[][]{},
              new Object[]{"Nome","Número da Conta", "Saldo em Carteira", "Assinatura"});

    
    
    private DefaultTableModel preencherTabela(ArrayList<Pedal> lista){
        
        for(int i = 0; i < lista.size(); i++ ){
            
            dtm.addRow(new String []{
                
                String.valueOf(lista.get(i).getNome()),
                String.valueOf(lista.get(i).getNumeroConta()),
                String.valueOf(lista.get(i).getSaldo()),
                String.valueOf(lista.get(i).GetAssinatura())
            });
        }
        return dtm;
    }
    
    private void showListFull(){
        jTable.setModel(preencherTabela(varLista));
    }
    
    private void limparTabela(){
        dtm.setNumRows(0);
    }
    
    private boolean pesquisar(){
        
        String pesquisa = jTextPesquisa.getText();
        limparTabela();
        
        if(pesquisa.equals("")){
            showListFull();
            return false;
        }
        
        jTable.setModel(preencherTabela(ciclista.procurar(pesquisa)));
        
        return true;
    }
    
    private boolean excluir(){
        int row = jTable.getSelectedRow();
        
        if(row < 0){
            JOptionPane.showMessageDialog(null,
                    "Selecione algum item da tabela.",
                    "Linha inválida",
                    JOptionPane.PLAIN_MESSAGE);
            return false;
        }
        
        if(ciclista.remover(jTable.getValueAt(row, 1).toString())){
            JOptionPane.showMessageDialog(null,
                    "Pedal excluido com Sucesso.",
                    "Excluido",
                    JOptionPane.PLAIN_MESSAGE);
            
        }else{
            JOptionPane.showMessageDialog(null,
                    "Pedal não pode ser excluido.",
                    "Algo errado ocorreu...",
                    JOptionPane.PLAIN_MESSAGE);
            return false;
        }
        limparTabela();
        showListFull();
        return true;
    }
    

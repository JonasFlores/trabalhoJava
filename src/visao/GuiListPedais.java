    private boolean prepareTranferencia(boolean isTranferir){
        
        if(isTranferir){

            int row = jTable.getSelectedRow();

            if(row < 0){

                JOptionPane.showMessageDialog(null,
                        "Selecione algum item da tabela.",
                        "Linha inválida",
                        JOptionPane.PLAIN_MESSAGE);
                return false;
            }

            jTextValor.setEnabled(true);
            jTextContaDestino.setEnabled(true);
            jButtonPagar.setEnabled(true);

            jTextPesquisa.setEnabled(false);
            jButtonTransferir.setEnabled(false);
            jButtonExcluir.setEnabled(false);
            jButtonPesquisa.setEnabled(false);
            jTable.setEnabled(false);
            jButtonRefresh.setEnabled(false);

            numContaOrigem = jTable.getValueAt(row, 1).toString();

            jTextContaOrigem.setText(numContaOrigem + " - " + 
                    jTable.getValueAt(row, 0).toString());

            jTextValor.setText(jTable.getValueAt(row, 2).toString());


            return true;
        }else{
            jTextValor.setEnabled(false);
            jTextContaDestino.setEnabled(false);
            jButtonPagar.setEnabled(false);

            jTextPesquisa.setEnabled(true);
            jButtonTransferir.setEnabled(true);
            jButtonExcluir.setEnabled(true);
            jButtonPesquisa.setEnabled(true);
            jTable.setEnabled(true);
            jButtonRefresh.setEnabled(true);

            jTextContaOrigem.setText("");
            jTextContaDestino.setText("");
            jTextValor.setText("");
            
            
            return true;
        }
        
    }
    
    private boolean transferir(){
        double valor = 0;
        String numContaDestino = jTextContaDestino.getText();
        String msg = "";
        String title = "Ocorreu um erro ao Tranferir";
        
        if(numContaDestino.isEmpty()){
            JOptionPane.showMessageDialog(null,
                    "Insira o número da conta de Destino.",
                    "Campo Obrigatório",
                    JOptionPane.PLAIN_MESSAGE);
            return false;
        }
        
        if(!jTextValor.getText().isEmpty()){
            valor = Double.parseDouble(jTextValor.getText().replace(",", "."));
        }
        
        msg = ciclista.transferir(numContaOrigem, valor, numContaDestino);
        
        
        if(msg.equals("")){
            msg = "Tranferência realizada com sucesso!";
            title = "Transeferido";
            limparTabela();
            showListFull();
            prepareTranferencia(false);
        }
        
        JOptionPane.showMessageDialog(null,
                    msg,
                    title,
                    JOptionPane.PLAIN_MESSAGE);
        
        
        return true;
    }
    
